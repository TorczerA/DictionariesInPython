#Dictionaries are Key-Value pairs.

print("Dictionaries are Key-Value pairs.")

d = {
    'word':'definition',
    'word2':'definition2',
    'word3' : 'definition3'
}

a = {
    'a':43,
    'b':56,
    'c':89
}

print("d =",d)
print("a =",a)
print("a['b'] =",a['b'])
print("d['word3'] =",d['word3'])